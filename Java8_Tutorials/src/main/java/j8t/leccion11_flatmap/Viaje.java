/**
 * The Class Viaje.
 * Link ejemplo:http://www.arquitecturajava.com/java-8-flatmap/
 */
public class Viaje {
	
	/** The pais. */
	private String pais;

	/**
	 * Instantiates a new viaje.
	 *
	 * @param pais the pais
	 */
	public Viaje(String pais) {
		super();
		this.pais = pais;
	}

	/**
	 * Gets the pais.
	 *
	 * @return the pais
	 */
	public String getPais() {
		return pais;
	}

	/**
	 * Sets the pais.
	 *
	 * @param pais the new pais
	 */
	public void setPais(String pais) {
		this.pais = pais;
	}
}
